import json
from pathlib import Path

RESULT_FILE = Path("results/qrbt_results.json")


def main():
    with open(RESULT_FILE, "r") as f:
        data = json.load(f)

    levels = sorted(map(int, data.keys()))

    print("% === QRBT-only Results Table (example) ===")
    print("\\begin{table}[!ht]")
    print("\\centering")
    print("\\caption{QRBT Performance Across Workload Levels}")
    print("\\begin{tabular}{|c|c|c|c|c|c|}")
    print("\\hline")
    print("Level & Latency Red. (\\%) & Security (\\%) & Scalability (\\%) & Energy (kWh) & RL Conv. (\\%)\\\\")
    print("\\hline")

    for lvl in levels:
        rec = data[str(lvl)]
        print(
            f"Level-{lvl} & "
            f"{rec['latency_reduction_pct']:.3f} & "
            f"{rec['security_pct']:.3f} & "
            f"{rec['scalability_pct']:.3f} & "
            f"{rec['energy_kwh']:.3f} & "
            f"{rec['convergence_score_pct']:.3f} \\\\"
        )
        print("\\hline")

    print("\\end{tabular}")
    print("\\end{table}")


if __name__ == '__main__':
    main()